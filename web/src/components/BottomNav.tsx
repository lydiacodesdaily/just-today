export function BottomNav() {
  const pathname = usePathname();

  // Hide navigation on Run page for focus mode
  if (pathname === '/run') {
    return null;
  }

  return (
    <nav
      className="md:hidden fixed bottom-0 left-0 right-0 bg-calm-surface border-t border-calm-border safe-area-bottom z-50"
      role="navigation"
      aria-label="Bottom navigation"
    >
      <div className="flex justify-around items-center h-16">
        {navItems.map((item) => {
          const isActive = pathname === item.href;
          return (
            <Link
              key={item.name}
              href={item.href}
              className={`
                flex flex-col items-center justify-center gap-1 px-3 py-2 min-w-[64px] min-h-[48px]
                transition-colors duration-200
                ${
                  isActive
                    ? 'text-calm-primary'
                    : 'text-calm-muted'
                }
              `}
              aria-current={isActive ? 'page' : undefined}
            >
              <span className={isActive ? 'text-calm-primary' : 'text-calm-muted'}>
                {item.icon}
              </span>
              <span className="text-xs font-medium">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
